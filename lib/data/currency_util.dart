  static String? currencyFromCode(String code) => _allCurrencies[code];

  static String? codeFromCurrency(String currency) {
    for (String code in _allCurrencies.keys) {
      if (_allCurrencies[code] == currency) {
        return code;
      }
    }

    return null;
  }
